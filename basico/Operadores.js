// uso de operadores dentro de js
/*
    operador de suma, resta, multiplicacion, division, modulo (residuo)
    potencia, incremento , decremento 

    simbolos ordenandos conforme a la lista anterior 
    +
    -
    *
    /
    %
    ** 
    ++
    --

*/

let a,b,c,d,e,f;

//SUma 
a = 3 + 4;
console.log(a);
//Resta
b = 2 -1;
console.log(b);
//mUltiplicacion 
c = 8 * 2;
console.log(c);
//division 
d = 7 - 5;
console.log(d);
