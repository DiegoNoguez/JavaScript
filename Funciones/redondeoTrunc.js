// Operadores de rendodeo y truncado en JS
let numero = 8.6, rendodeo, truncado;

// redondeo
// math.round() redondea al valor entero mas cercano

rendodeo= Math.round(numero);
console.log(rendodeo);

// Truncado
//  math.trunc elimina la parte flotante 
truncado = Math.trunc(truncado);
console.log(truncado);